package org.example;

public class _03_Variables {
    public static void main(String[] args) {
        int age = 25;
        int schoolNumber = 55;
        System.out.println("Age: " + age + " School Number: "+schoolNumber);

        int _schoolNumber = 154;
        System.out.println(_schoolNumber);

        int $schoolNumber = 221;
        System.out.println($schoolNumber);

    }
    // veri değişken isimlerini yazarken
        /*
            veri isimlendirmeleri;
            1) isim sıfat veya zamir ile başla
            2) sayı ile bitebilir
            3) sayı ile başlanamaz
            4) _ veya $ dışında özel simge kullanılamaz
            5) variable camelCase ile yazılır
         */
}
